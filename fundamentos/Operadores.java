public class Operadores {
    public static void main(String[] args) {
        System.out.print("2 + 2 == 4   ");
        System.out.println(2 + 2 == 4);
        System.out.print("2 + 2 != 4   ");
        System.out.println(2 + 2 != 4);
        System.out.print("2 + 3 >= 4   ");
        System.out.println(2 + 3 >= 4);
        System.out.print("2 + 3 <= 4   ");
        System.out.println(2 + 3 <= 4);
        System.out.print("!(2 + 3 >= 4)   "); // msm coisa q not
        System.out.println(!(2 + 3 >= 4));
        System.out.print("2 == 4 || 2 == 2   "); // ou
        System.out.println(2 == 4 || 2 == 2);
        System.out.print("2 == 4 && 2 == 2   "); // e
        System.out.println(2 == 4 && 2 == 2);
    }
}
